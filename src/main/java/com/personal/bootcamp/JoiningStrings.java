package com.personal.bootcamp;

public class JoiningStrings {
    public static void main(String[] args) {
        final String sentence = "His name is: " ;
        final String placeholder = "Harry";
        System.out.println(sentence + placeholder);

        final Integer points = 50;
        final String announcement = " points for Gryffindor";
        System.out.println(points + announcement);
    }
}
